package JavaAssignment;

public class Loops {

	public static void main(String[] args) 
	
	{
		System.out.println("****************Print by For Loop***************************");
		int i=10;
		while (i>=1)
		{
			System.out.println(i);
			i--;
		}
		
		System.out.println("****************Print by For Loop***************************");
		
		for(int n=10;n>=1;n--)
		{
			System.out.println(n);
		}
		
		System.out.println("****************Print by do while Loop***************************");
		
		int l=10;
		
		do
		{
			System.out.println(l);
			l--;
		}
		while(l>=1);

	}

}
